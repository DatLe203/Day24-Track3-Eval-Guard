from __future__ import annotations

"""Phase A: RAGAS production evaluation for the 50-question test set."""

import json
import os
import sys
from dataclasses import dataclass

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from config import ANSWERS_PATH, TEST_SET_PATH

Distribution = str

METRICS = ("faithfulness", "answer_relevancy", "context_precision", "context_recall")

DIAGNOSTIC_TREE = {
    "faithfulness": ("LLM hallucinating", "Tighten system prompt and require evidence from retrieved context"),
    "context_recall": ("Missing relevant chunks", "Improve chunking, hybrid retrieval, or metadata enrichment"),
    "context_precision": ("Too many irrelevant chunks", "Add reranking or stronger metadata filters"),
    "answer_relevancy": ("Answer does not match the question", "Improve prompt template and preserve user intent"),
}


@dataclass
class RagasResult:
    question_id: int
    distribution: Distribution
    question: str
    answer: str
    contexts: list[str]
    ground_truth: str
    faithfulness: float
    answer_relevancy: float
    context_precision: float
    context_recall: float

    @property
    def avg_score(self) -> float:
        return sum(getattr(self, metric) for metric in METRICS) / len(METRICS)

    @property
    def worst_metric(self) -> str:
        return min(METRICS, key=lambda metric: getattr(self, metric))


def load_test_set_50q(path: str = TEST_SET_PATH) -> list[dict]:
    """Load the 50-question evaluation set."""
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def load_answers(path: str = ANSWERS_PATH) -> list[dict]:
    """Load answers generated by setup_answers.py."""
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"answers_50q.json not found at {path}. Run python setup_answers.py first."
        )
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def group_by_distribution(test_set: list[dict]) -> dict[str, list[dict]]:
    """Group questions by the expected distributions."""
    groups = {"factual": [], "multi_hop": [], "adversarial": []}
    for item in test_set:
        dist = item.get("distribution")
        if dist in groups:
            groups[dist].append(item)
    return groups


def run_ragas_50q(answers: list[dict]) -> list[RagasResult]:
    """Run Day 18 evaluate_ragas() and map per-question metrics to RagasResult."""
    from src.m4_eval import evaluate_ragas

    questions = [item.get("question", "") for item in answers]
    answer_texts = [item.get("answer", "") for item in answers]
    contexts = [item.get("contexts", []) or [] for item in answers]
    ground_truths = [item.get("ground_truth", "") for item in answers]

    raw = evaluate_ragas(questions, answer_texts, contexts, ground_truths)
    per_question = raw.get("per_question", [])

    def read_metric(row, name: str) -> float:
        if isinstance(row, dict):
            return float(row.get(name, 0.0) or 0.0)
        return float(getattr(row, name, 0.0) or 0.0)

    results: list[RagasResult] = []
    for item, row in zip(answers, per_question):
        results.append(RagasResult(
            question_id=int(item.get("id", len(results) + 1)),
            distribution=item.get("distribution", "unknown"),
            question=item.get("question", ""),
            answer=item.get("answer", ""),
            contexts=item.get("contexts", []) or [],
            ground_truth=item.get("ground_truth", ""),
            faithfulness=read_metric(row, "faithfulness"),
            answer_relevancy=read_metric(row, "answer_relevancy"),
            context_precision=read_metric(row, "context_precision"),
            context_recall=read_metric(row, "context_recall"),
        ))
    return results


def bottom_10(results: list[RagasResult]) -> list[dict]:
    """Return up to 10 lowest-scoring questions with diagnosis and fix."""
    rows = []
    for rank, result in enumerate(sorted(results, key=lambda item: item.avg_score)[:10], start=1):
        diagnosis, suggested_fix = DIAGNOSTIC_TREE[result.worst_metric]
        rows.append({
            "rank": rank,
            "question_id": result.question_id,
            "distribution": result.distribution,
            "question": result.question,
            "avg_score": round(result.avg_score, 4),
            "worst_metric": result.worst_metric,
            "diagnosis": diagnosis,
            "suggested_fix": suggested_fix,
        })
    return rows


def cluster_analysis(results: list[RagasResult]) -> dict:
    """Build a worst_metric x distribution failure matrix and short insight."""
    matrix = {
        metric: {"factual": 0, "multi_hop": 0, "adversarial": 0}
        for metric in METRICS
    }
    for result in results:
        if result.worst_metric in matrix and result.distribution in matrix[result.worst_metric]:
            matrix[result.worst_metric][result.distribution] += 1

    distributions = ("factual", "multi_hop", "adversarial")
    dominant_dist = max(
        distributions,
        key=lambda dist: sum(metric_counts[dist] for metric_counts in matrix.values()),
    )
    dominant_metric = max(matrix, key=lambda metric: sum(matrix[metric].values()))
    insight = (
        f"Distribution '{dominant_dist}' has the most failures. "
        f"The dominant weak metric is '{dominant_metric}'. "
        f"Suggested fix: {DIAGNOSTIC_TREE[dominant_metric][1]}."
    )
    return {
        "matrix": matrix,
        "dominant_failure_distribution": dominant_dist,
        "dominant_failure_metric": dominant_metric,
        "insight": insight,
    }


def save_phase_a_report(results: list[RagasResult], clusters: dict,
                        path: str = "reports/ragas_50q.json") -> None:
    """Save Phase A report to JSON."""
    os.makedirs(os.path.dirname(path), exist_ok=True)

    per_distribution: dict[str, dict] = {}
    for dist in ("factual", "multi_hop", "adversarial"):
        subset = [item for item in results if item.distribution == dist]
        if not subset:
            continue
        per_distribution[dist] = {
            "count": len(subset),
            **{
                metric: round(sum(getattr(item, metric) for item in subset) / len(subset), 4)
                for metric in METRICS
            },
            "avg_score": round(sum(item.avg_score for item in subset) / len(subset), 4),
        }

    report = {
        "total_questions": len(results),
        "per_distribution": per_distribution,
        "failure_clusters": clusters,
        "bottom_10": bottom_10(results),
    }
    with open(path, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)
    print(f"Phase A report saved -> {path}")


if __name__ == "__main__":
    test_set = load_test_set_50q()
    print(f"Loaded {len(test_set)} questions")

    groups = group_by_distribution(test_set)
    for dist, questions in groups.items():
        print(f"  {dist}: {len(questions)} questions")

    answers = load_answers()
    results = run_ragas_50q(answers)
    clusters = cluster_analysis(results)
    save_phase_a_report(results, clusters)

    print("\nBottom 10 worst questions:")
    for item in bottom_10(results):
        print(
            f"  #{item['rank']} [{item['distribution']}] "
            f"avg={item['avg_score']:.3f} worst={item['worst_metric']} "
            f"{item['question'][:60]}..."
        )
    print(
        f"\nDominant failure: {clusters['dominant_failure_distribution']} / "
        f"{clusters['dominant_failure_metric']}"
    )
